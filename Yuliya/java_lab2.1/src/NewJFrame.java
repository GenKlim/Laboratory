    private final List<fly> LC;                     // Список для хранения созданных Тараканов
    private final Timer activeTimer;                // Таймер для исполнения комманд
    private int AlgStep = 0;
    
    public NewJFrame() {
        listmodel = new javax.swing.DefaultListModel();
        LC = new ArrayList<>();
        
        //Грузит картинки
        try {
            image = ImageIO.read(new File("fly.jpg")); 
            image_disable = ImageIO.read(new File("fly_disable.jpg")); 
        } catch (IOException e) {
            System.out.println("Image not found");
        }
        
        initComponents();
        
        activeTimer = new Timer(speed, (ActionEvent e) -> algStep());
        jList1.setModel(listmodel);
    }
    
    private void algStep()
    {
        if(listmodel.size() > AlgStep)
        {
            jList1.setSelectedIndex(AlgStep);
            String cmd = jList1.getSelectedValue().toString();

            LC.stream().forEach(f -> f.Eval(cmd));  //Вызовит Eval для каждой мухи
            
            AlgStep++;
        }
        else
            activeTimer.stop();
    }

